                   <h2>শিশুর টিকা প্রদানের তালিকা</h2>
                   <h3>1. বিসিজি</h3>
                   <p>বিসিজি টিকা বাচ্চার জন্মের পর থেকে ১৫ দিন সময়ের মধ্যে যে কোনও সময় দেওয়া যায়। এই টিকা টিউবারকুলোসিস মেনিনজাইটিস (Tuberculous Meningitis) বা বাচ্চাদের একরকম যক্ষ্মা রোগ থেকে রক্ষা করে। ইঞ্জেকশনের মাধ্যমে এই বিসিজি টিকা বাচ্চাকে দেওয়া হয়ে থাকে।আদর্শ বিসিজির ডোজে ১ মিলিলিটার টিকার তরলে ০.১ মিলিগ্রাম ওষুধ থাকে।বাচ্চাকে একটাই বিসিজি ডোজ দেওয়া হয়।</p>
                   <h3>2. হেপাটাইটিস বি টিকা </h3>
                   <p>আমাদের শরীরে হেপাটাইটিস বি নামক ভাইরাসের আক্রমণ হলে, লিভার প্রচণ্ডভাবে ক্ষতিগ্রস্ত হয়। লিভার ক্যান্সার হতে পারে, লিভার কাজ করা বন্ধ করে দেয়, এমনকী মৃত্যুও হতে পারে। রোগীর অজান্তেই এই রোগ অন্যদের মধ্যেও সংক্রামিত হতে পারে। এই সব রোগের বিরুদ্ধে শিশুর প্রতিরোধ ক্ষমতা বাড়ানোর জন্য তিনটি ডোজে হেপাটাইটিস বি টিকা দেওয়া হয়। জন্মের এক-দু’দিনের মধ্যে প্রথম ডোজ, শিশুর বয়স এক মাস হলে দ্বিতীয় ডোজ এবং ৬ মাস হলে তৃতীয় বা শেষ ডোজটি দেওয়া হয়।</p>
                   <h3>3. পোলিও টিকা </h3>
                   <p>এই টিকাটি খুবই গুরুত্বপূর্ণ এবং পোলিও রোগের হাত থেকে শিশুকে রক্ষা করে। পোলিও রোগের প্রকোপে শিশুর পা বিকৃত হয়ে যায় এবং সারাজীবনের জন্য শিশু প্যারালাইজড হয়ে যেতে পারে। এই পোলিও টিকা চারটি ডোজে শিশুকে দেওয়া হয়। প্রথম ডোজ জন্মের পর থেকে ২ মাসের মধ্যে, দ্বিতীয় ডোজ ৪ মাস বয়সে, তৃতীয় ডোজ ৬-১৮ মাসের মধ্যে এবং শেষ ডোজটি বুস্টার ডোজ হিসেবে দেওয়া হয় ৪-৬ বছর বয়সের মধ্যে। ইঞ্জেকশনের মাধ্যমে এই ডোজগুলি দেওয়া যায় আবার ওষুধের মতো খাইয়েও দেওয়া যায়।</p>
                   <h3>4. ডিটিএপি/ডিটিপি</h3>
                   <p>এই টিকাটি টিটেনাস (Tetanus), ডিপথেরিয়া(Diphtheria) ও হুপিং কাশি(whooping cough) –এর মতো রোগ প্রতিরোধ করার ক্ষমতা শরীরে গড়ে তোলে। বাচ্চার ১.৫ মাস, ২.৫ মাস ও ৩.৫ মাস বয়সে এই টিকাটি দেওয়া হয়ে থাকে। এরপর, ১.৫ বছর বয়সে DPT-র দ্বিতীয় ডোজ দেওয়া হয় এবং ৫ বছর বয়সে Double DT-র আর একটি ডোজ দেওয়া হয়। DTaP টিকাই বর্তমানে ব্যবহার করা হয়, কারণ এটায় শিশু ব্যথা পায় না এবং আগের থেকে নিরাপদ। এর আগে শিশুকে DTP টিকা দেওয়া হত, এই DTP টিকা শিশুর জন্য খুবই যন্ত্রণাদায়ক ছিল।</p>
                   <h3>5. রোটাভাইরাস টিকা </h3>
                   <p>রোটা ভাইরাসের আক্রমণে শিশুর মারাত্মক ডিহাইড্রেশন ও ডায়রিয়া হতে পারে। শিশুকে এই রোটাভাইরাস টিকা ওষুধের মতো খাওয়ানো হয়ে থাকে। শিশুর জন্মের ৬-১৫ সপ্তাহের মধ্যে এই টিকার প্রথম ডোজ খাওয়ানো হয়। মোট তিনটি ডোজে এই টিকা শিশুকে দেওয়া হয়। শিশুর ২ মাস বয়সে একটি, ৪ মাস বয়সে একটি এবং ৬ মাস বয়সে একটি, মোট তিনটি ডোজ দেওয়া হয়।</p>
                   <h3>6. এইচ আই বি টিকা </h3>
                   <p>এই টিকা নিলে শিশুর শরীর হেমোফিলাস ইনফ্লুয়েঞ্জা (Haemophilus Influenzae) নামক ব্যাকটেরিয়ার বিরুদ্ধে প্রতিরোধ ক্ষমতা গড়ে তুলতে পারে। এর আক্রমণে শিশুর ত্বক, গলা এবং মস্তিস্ক ক্ষতিগ্রস্ত হয়। মেনিনজাইটিস, এপিগ্লটিস এবং নিউমোনিয়ার মতো রোগ থেকে শিশুকে রক্ষা করে।এই টিকাটিও বাচ্চাকে তিনটি ডোজে দেওয়া হয়ে থাকে। বাচ্চার ২ মাস এবং ৪ মাস বয়সে যথাক্রমে প্রথম ও দ্বিতীয় ডোজ দেওয়া হয়। বাচ্চার বয়স ১৫ মাস হয়ে গেলে শেষ ডোজটি বুস্টার ডোজ হিসেবে দেওয়া হয়ে থাকে।</p>
